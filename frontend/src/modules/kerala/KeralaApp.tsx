const ALLOWED_SOURCE_FILES = new Set([
  "predictions_2026.csv",
  "kerala_prediction_scenarios_2026.csv",
]);
// Backend "confidence" is now the top-1 predicted-party probability
// (range 0.25-1.0 for 4 classes). 0.60 cleanly separates confident calls
// from competitive ones; the 0.75 margin used previously was unreachable.
const HIGH_MARGIN_THRESHOLD = 0.6;

function getSeatCounts(rows: PredictionRow[]) {
  const counts: Record<Party, number> = {
    LDF: 0,
    UDF: 0,
    NDA: 0,
    OTHERS: 0,
  };

  for (const row of rows) counts[row.predicted] += 1;
  return counts;
}

function toCountsLine(counts: Record<Party, number>): string {
  return `LDF ${counts.LDF}, UDF ${counts.UDF}, NDA ${counts.NDA}, OTHERS ${counts.OTHERS}`;
}

function seatCountsMatch(a: Record<Party, number>, b: Record<Party, number>): boolean {
  return (
    a.LDF === b.LDF &&
    a.UDF === b.UDF &&
    a.NDA === b.NDA &&
    a.OTHERS === b.OTHERS
  );
}

type MetaValidationResult = {
  error: string | null;
  warning: string | null;
};

function validatePredictionMeta(meta: PredictionsMeta): MetaValidationResult {
  // VITE_EXPECTED_PREDICTIONS_SHA256 is an optional integrity check — when
  // unset, skip the comparison and rely on the console warning emitted in
  // services/api.ts. Setting it lets you detect stale backends.

  if (meta.fallback_in_use) {
    return {
      error: "Backend is serving fallback data (kerala_assembly_2026.csv), not final predictions_2026.csv.",
      warning: null,
    };
  }

  if (!ALLOWED_SOURCE_FILES.has(meta.source_file)) {
    return {
      error: `Unexpected prediction source file: ${meta.source_file}. Expected one of: ${[
        ...ALLOWED_SOURCE_FILES,
      ].join(", ")}.`,
      warning: null,
    };
  }

  if (meta.total_constituencies !== 140) {
    return {
      error: `Unexpected constituency count: ${meta.total_constituencies}. Expected 140.`,
      warning: null,
    };
  }

  if (EXPECTED_API_VERSION && meta.api_version && meta.api_version !== EXPECTED_API_VERSION) {
    return {
      error: `Backend API version mismatch. Expected ${EXPECTED_API_VERSION}, got ${meta.api_version}.`,
      warning: null,
    };
  }

  // SHA256 pin only applies when the backend serves the trained-model file
  // directly. With an active scenario overlay (kerala_prediction_scenarios_*),
  // the served file is regenerated by build_scenarios.py and is not hash-pinned.
  if (
    EXPECTED_PREDICTIONS_SHA256 &&
    meta.source_file === "predictions_2026.csv" &&
    (!meta.source_sha256 ||
      meta.source_sha256.toLowerCase() !== EXPECTED_PREDICTIONS_SHA256.toLowerCase())
  ) {
    const actualHash = meta.source_sha256 ? meta.source_sha256.toLowerCase() : "missing";
    return {
      error: null,
      warning:
        `Backend predictions hash mismatch. Expected ${EXPECTED_PREDICTIONS_SHA256}, got ${actualHash}. ` +
        "Using live backend data anyway; redeploy backend and frontend from the same commit.",
    };
  }

  return { error: null, warning: null };
}

export function KeralaApp() {
  useSeo({
    title: "Owlytics Election Prediction 2026",
    description:
      "AI-led, constituency-level forecast for the Kerala 2026 Assembly Election. Seat projections and vote shares for LDF, UDF, and NDA across 140 constituencies and 14 districts.",
    canonicalPath: "/kerala",
  });

  // ── Active analysis lens (drives KPI tile + constituency table + bars) ──
  // Each tab maps to a different pre-built CSV on the backend, so the
  // entire dashboard swaps when the user changes tabs (mirrors TN behaviour).
  const [activeLens, setActiveLens] = useState<LensName>("historical_projection");
  const [lensSummaryByName, setLensSummaryByName] = useState<
    Partial<Record<LensName, LensSummary>>
  >({});

  const activeSummary = lensSummaryByName[activeLens];
  const rows: PredictionRow[] = activeSummary?.rows ?? [];

  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);
  const [warning, setWarning] = useState<string | null>(null);

  const [district, setDistrict] = useState("ALL");
  const [party, setParty] = useState<Party | "ALL">("ALL");
  const [query, setQuery] = useState("");
  const [searchOpen, setSearchOpen] = useState(false);
  const [districtOpen, setDistrictOpen] = useState(false);
  const [partyOpen, setPartyOpen] = useState(false);
  const searchRef = useRef<HTMLDivElement | null>(null);
  const districtRef = useRef<HTMLDivElement | null>(null);
  const partyRef = useRef<HTMLDivElement | null>(null);
  const middleStageRef = useRef<HTMLElement | null>(null);
  const prefersReducedMotion = useReducedMotion();
  const deferredQuery = useDeferredValue(query);

  useEffect(() => {
    if (!searchOpen && !districtOpen && !partyOpen) return;
    const handler = (e: MouseEvent) => {
      const target = e.target as Node;
      if (searchOpen && searchRef.current && !searchRef.current.contains(target)) {
        setSearchOpen(false);
      }
      if (districtOpen && districtRef.current && !districtRef.current.contains(target)) {
        setDistrictOpen(false);
      }
      if (partyOpen && partyRef.current && !partyRef.current.contains(target)) {
        setPartyOpen(false);
      }
    };
    document.addEventListener("mousedown", handler);
    return () => document.removeEventListener("mousedown", handler);
  }, [searchOpen, districtOpen, partyOpen]);

  // ── Initial load: health + meta validation, plus all 4 lens datasets in parallel.
  // Per-lens datasets are cached in lensSummaryByName so tab switches are instant.
  useEffect(() => {
    const controller = new AbortController();

    async function load() {
      setLoading(true);
      setError(null);
      setWarning(null);

      try {
        // Kick off health + batch lenses in parallel — neither blocks the other.
        const healthPromise = fetchHealth(controller.signal);
        const lensesPromise = fetchAllLensSummaries(controller.signal);

        // Render the dashboard as soon as lens data lands; meta validation
        // continues in the background and only surfaces a warning/error if
        // it actually fails.
        const next = await lensesPromise;
        if (controller.signal.aborted) return;
        if (Object.keys(next).length === 0) {
          throw new Error("No lens datasets could be loaded from the backend.");
        }
        setLensSummaryByName(next);
        setLoading(false);

        // Validate health + meta after first paint. Errors get surfaced as a
        // banner; the dashboard stays usable in the meantime.
        const health = await healthPromise;
        if (controller.signal.aborted) return;
        if (health.status !== "ok") {
          setError(health.error || "Backend health check failed.");
          return;
        }
        let meta = health.meta;
        if (!meta) {
          meta = await fetchPredictionsMeta(controller.signal);
          if (controller.signal.aborted) return;
        }
        const validation = validatePredictionMeta(meta);
        if (validation.warning) setWarning(validation.warning);
        if (validation.error) setError(validation.error);
      } catch (err) {
        if (
          controller.signal.aborted ||
          (err instanceof DOMException && err.name === "AbortError") ||
          (err instanceof Error && err.name === "AbortError")
        ) {
          return;
        }
        const message = err instanceof Error ? err.message : "Unknown error";
        setError(`${message} Ensure backend is running on ${API_BASE}.`);
      } finally {
        if (!controller.signal.aborted) setLoading(false);
      }
    }

    void load();
    return () => controller.abort();
  }, []);

  const districts = useMemo(() => {
    return [...new Set(rows.map((r) => r.district))].sort((a, b) => a.localeCompare(b));
  }, [rows]);

  const constituencyOptions = useMemo(() => {
    return [...new Set(rows.map((r) => r.constituency))]
      .sort((a, b) => a.localeCompare(b));
  }, [rows]);

  const filteredRows = useMemo(() => {
    const q = deferredQuery.trim().toLowerCase();

    const next = rows.filter((r) => {
      const districtOk = district === "ALL" || r.district === district;
      const partyOk = party === "ALL" || r.predicted === party;
      const queryOk = q.length === 0 || r.constituency.toLowerCase().includes(q);
      return districtOk && partyOk && queryOk;
    });

    next.sort((a, b) => b.confidence - a.confidence);

    return next;
  }, [rows, district, party, deferredQuery]);

  const seatCounts = useMemo(() => getSeatCounts(filteredRows), [filteredRows]);

  // The "Seat Distribution" bar list below uses `seatCounts` derived from the
  // active /api/predictions rows (count-of-winners over `predicted`). The 4
  // hero KPI tabs each fetch their own /api/predictions/lens summary from
  // AnalysisHero, so per-tab seat distributions vary visibly.
  // Always show LDF / UDF / NDA / OTHERS — even when a lens scores zero seats
  // for some of them. Matches Tamil Nadu's behaviour, which keeps all 5
  // alliance bars on screen regardless of count.
  const displayParties: Party[] = ["LDF", "UDF", "NDA", "OTHERS"];
  const sortedParties = useMemo(() => {
    // Sort by seat count desc, but pin OTHERS to the last position so the
    // visual ordering stays consistent across tabs (mirrors TN).
    return [...displayParties].sort((a, b) => {
      if (a === "OTHERS") return 1;
      if (b === "OTHERS") return -1;
      return seatCounts[b] - seatCounts[a];
    });
  }, [seatCounts]);
  const total = filteredRows.length;
  const safeTotal = total || 1;
  const highMarginSeats = useMemo(() => {
    return filteredRows.reduce(
      (count, row) =>
        count + (row.confidence >= HIGH_MARGIN_THRESHOLD ? 1 : 0),
      0,
    );
  }, [filteredRows]);

  const closestSeats = useMemo(() => {
    // Sort lowest-confidence first (most competitive). The lens CSVs can give
    // many ACs the exact same score (Recent Swing pulls LS2024 + LB2025 inputs
    // that are aggregated at Lok-Sabha-constituency granularity, so all ~7 ACs
    // sharing a parent LS get identical scores). Without diversification this
    // panel collapses to a single district at the bottom of the ranking.
    const ascending = [...filteredRows].sort((a, b) => a.confidence - b.confidence);
    const perDistrictCap = 2;
    const perDistrictCount = new Map<string, number>();
    const picked: typeof ascending = [];
    for (const row of ascending) {
      const used = perDistrictCount.get(row.district) ?? 0;
      if (used >= perDistrictCap) continue;
      picked.push(row);
      perDistrictCount.set(row.district, used + 1);
      if (picked.length >= 8) break;
    }
    return picked;
  }, [filteredRows]);

  const districtBreakdown = useMemo(() => {
    const map = new Map<string, Record<Party, number>>();

    for (const row of filteredRows) {
      if (!map.has(row.district)) {
        map.set(row.district, { LDF: 0, UDF: 0, NDA: 0, OTHERS: 0 });
      }
      map.get(row.district)![row.predicted] += 1;
    }

    return [...map.entries()]
      .map(([name, counts]) => ({ name, ...counts, total: counts.LDF + counts.UDF + counts.NDA + counts.OTHERS }))
      .sort((a, b) => a.name.localeCompare(b.name));
  }, [filteredRows]);

  // Replay the middle-stage entrance animation whenever the active lens
  // changes (and on initial reveal once data is ready), so each tab switch
  // shows the bars growing in.
  useEffect(() => {
    const element = middleStageRef.current;
    if (!element || loading || error) return;
    element.classList.remove("animate-cards");
    // Force reflow so the next class addition restarts the CSS animation.
    void element.offsetWidth;
    element.classList.add("animate-cards");
  }, [loading, error, activeLens]);

  return (
    <div className="kerala-app app-shell">
      <div className="bg-blur bg-blur-a" />
      <div className="bg-blur bg-blur-b" />

      <main className="container">
        <AnalysisHero
          activeLens={activeLens}
          onLensChange={setActiveLens}
          summaries={lensSummaryByName}
        />

        {error && <div className="error-banner">{error}</div>}
        {!error && warning && <div className="warning-banner">{warning}</div>}
        {loading && <div className="panel loading">Loading predictions...</div>}

        {!loading && !error && (
          <>
            <section className="middle-stage" ref={middleStageRef}>
              <aside className="left-stack">
                <article className="panel">
                  <h2>District Breakdown</h2>
                  <div className="district-list">
                    {districtBreakdown.map((d) => (
                      <div key={d.name} className="district-item">
                        <div className="district-head">
                          <strong>{d.name}</strong>
                          <span>{d.total} seats</span>
                        </div>
                        <div className="district-bars">
                          {displayParties.map((p) => (
                            <div
                              key={p}
                              className={`district-segment segment-${p}`}
                              style={{ width: `${(d[p] / (d.total || 1)) * 100}%` }}
                              title={`${p}: ${d[p]}`}
                            />
                          ))}
                        </div>
                      </div>
                    ))}
                  </div>
                </article>
              </aside>

              <article className="panel center-card">
                <div className="center-inner-grid">
                  <section className="inner-block filters-block">
                    <h2>Filters</h2>
                    <div className="filters-grid">
                      <div className="combo-wrap" ref={districtRef}>
                        <label htmlFor="district">District</label>
                        <button
                          id="district"
                          type="button"
                          className="combo-toggle"
                          aria-haspopup="listbox"
                          aria-expanded={districtOpen}
                          onClick={() => setDistrictOpen((o) => !o)}
                        >
                          <span>{district === "ALL" ? "All Districts" : district}</span>
                          <span className="combo-chevron" aria-hidden="true">▾</span>
                        </button>
                        {districtOpen && (
                          <ul className="combo-list" role="listbox">
                            <li
                              role="option"
                              aria-selected={district === "ALL"}
                              className="combo-item"
                              onClick={() => {
                                setDistrict("ALL");
                                setDistrictOpen(false);
                              }}
                            >
                              All Districts
                            </li>
                            {districts.map((d) => (
                              <li
                                key={d}
                                role="option"
                                aria-selected={district === d}
                                className="combo-item"
                                onClick={() => {
                                  setDistrict(d);
                                  setDistrictOpen(false);
                                }}
                              >
                                {d}
                              </li>
                            ))}
                          </ul>
                        )}
                      </div>

                      <div className="combo-wrap" ref={partyRef}>
                        <label htmlFor="party">Predicted Party</label>
                        <button
                          id="party"
                          type="button"
                          className="combo-toggle"
                          aria-haspopup="listbox"
                          aria-expanded={partyOpen}
                          onClick={() => setPartyOpen((o) => !o)}
                        >
                          <span>{party === "ALL" ? "All Parties" : party}</span>
                          <span className="combo-chevron" aria-hidden="true">▾</span>
                        </button>
                        {partyOpen && (
                          <ul className="combo-list" role="listbox">
                            <li
                              role="option"
                              aria-selected={party === "ALL"}
                              className="combo-item"
                              onClick={() => {
                                setParty("ALL");
                                setPartyOpen(false);
                              }}
                            >
                              All Parties
                            </li>
                            {displayParties.map((p) => (
                              <li
                                key={p}
                                role="option"
                                aria-selected={party === p}
                                className="combo-item"
                                onClick={() => {
                                  setParty(p);
                                  setPartyOpen(false);
                                }}
                              >
                                {p}
                              </li>
                            ))}
                          </ul>
                        )}
                      </div>

                      <div className="combo-wrap" ref={searchRef}>
                        <label htmlFor="search">Search Constituency</label>
                        <button
                          id="search"
                          type="button"
                          className="combo-toggle"
                          aria-haspopup="listbox"
                          aria-expanded={searchOpen}
                          onClick={() => setSearchOpen((o) => !o)}
                        >
                          <span>{query || "Select Constituency"}</span>
                          <span className="combo-chevron" aria-hidden="true">▾</span>
                        </button>
                        {searchOpen && constituencyOptions.length > 0 && (
                          <ul className="combo-list" role="listbox">
                            <li
                              role="option"
                              aria-selected={query.length === 0}
                              className="combo-item"
                              onClick={() => {
                                setQuery("");
                                setSearchOpen(false);
                              }}
                            >
                              All Constituencies
                            </li>
                            {constituencyOptions.map((name) => (
                              <li
                                key={name}
                                role="option"
                                aria-selected={query === name}
                                className="combo-item"
                                onClick={() => {
                                  setQuery(name);
                                  setSearchOpen(false);
                                }}
                              >
                                {name}
                              </li>
                            ))}
                          </ul>
                        )}
                      </div>
                    </div>
                  </section>

                  <section className="inner-block seat-distribution-block">
                    <h2>Seat Distribution</h2>
                    <div className="bar-list">
                      {sortedParties.map((p) => (
                        <div className="bar-item" key={p}>
                          <div className="bar-top">
                            <span>{p}</span>
                            <span>
                              {seatCounts[p]} ({asSeatPercent(seatCounts[p], safeTotal)})
                            </span>
                          </div>
                          <div className="bar-track">
                            <div
                              className={`bar-fill fill-${p}`}
                              style={{ width: `${(seatCounts[p] / safeTotal) * 100}%` }}
                            />
                          </div>
                        </div>
                      ))}
                    </div>
                  </section>
                </div>
              </article>

              <aside className="right-stack">
                <article className="panel">
                  <h2>Most Competitive Seats</h2>
                  <ul className="tight-list">
                    {closestSeats.map((seat) => (
                      <li key={seat.constituency}>
                        <div>
                          <strong>{seat.constituency}</strong>
                          <small>{seat.district}</small>
                        </div>
                        <div className="right-inline">
                          <PartyBadge party={seat.predicted} />
                          <span>{asPercentSmart(seat.confidence)} Score</span>
                        </div>
                      </li>
                    ))}
                  </ul>
                </article>
              </aside>
            </section>

            <motion.section
              className="panel table-panel explorer-section"
              initial={prefersReducedMotion ? false : { opacity: 1, y: 20 }}
              whileInView={prefersReducedMotion ? undefined : { opacity: 1, y: 0 }}
              viewport={{ once: true, amount: 0.15 }}
              transition={{ duration: 0.45, ease: [0.22, 1, 0.36, 1] }}
            >
              <motion.div
                className="table-head"
                initial={prefersReducedMotion ? false : { opacity: 1, y: 10 }}
                whileInView={prefersReducedMotion ? undefined : { opacity: 1, y: 0 }}
                viewport={{ once: true, amount: 0.2 }}
                transition={{ duration: 0.35, delay: 0.05, ease: [0.22, 1, 0.36, 1] }}
              >
                <div className="explorer-title-block">
                  <h2 className="explorer-title">
                    Constituency-level {activeSummary?.label ?? "Historical Projection"}
                  </h2>
                  <p className="explorer-subtitle">{rows.length} constituencies analysed</p>
                </div>
              </motion.div>
              <motion.div
                className="table-wrap"
                initial={prefersReducedMotion ? false : { opacity: 1, y: 14 }}
                whileInView={prefersReducedMotion ? undefined : { opacity: 1, y: 0 }}
                viewport={{ once: true, amount: 0.1 }}
                transition={{ duration: 0.38, delay: 0.12, ease: [0.22, 1, 0.36, 1] }}
              >
                <table>
                  <thead>
                    <tr>
                      <th>AC No</th>
                      <th>Constituency</th>
                      <th>District</th>
                      <th>Predicted</th>
                      <th>Winning Score</th>
                      {displayParties.map((p) => (
                        <th key={p}>{p}</th>
                      ))}
                    </tr>
                  </thead>
                  <tbody>
                    {filteredRows.map((row) => (
                        <tr key={row.constituency}>
                          <td>{row.ac_no ?? "-"}</td>
                          <td>{row.constituency}</td>
                          <td>{row.district}</td>
                          <td>
                            <PartyBadge party={row.predicted} />
                          </td>
                          <td>{asPercentSmart(row.confidence)}</td>
                          {displayParties.map((p) => (
                            <td key={p}>{asPercentPrecise(row[p])}</td>
                          ))}
                        </tr>
                    ))}
                  </tbody>
                </table>
              </motion.div>
            </motion.section>
          </>
        )}
      </main>
    </div>
  );
}

export default KeralaApp;
